int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int i;

	i = 0;
	while (s1[n] || s2[n])
	{
		if (s1[n] != s2[n])
			return (s1[i] - s2[i]);
			i++;
		}
	return (0);
}

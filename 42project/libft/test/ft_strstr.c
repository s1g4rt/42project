char *ft_strstr(char *str,char *to_find)
{

	int a;
	int b;

	a = 0;
	while (str[a] != '\0')
	{
		b = 0;
		while (to_find[b] == str[a])
		{
			if (to_find[b] == '\0')
			{
				return(str);
			}
			b++;
		}
		a++;
	}
	return(0);	
}
